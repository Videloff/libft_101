#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned int	i;

	i = 0;
	while (s[i] || c == '\0')
	{
		if (s[i] == c)
			return (&((char *)s)[i]);
		i++;
	}
	return (NULL);
}
